static void try_input(u32 input_idx) {
    mkb::pad_status_groups[0].raw.stickX = s_possible_inputs[input_idx].x;
    mkb::pad_status_groups[0].raw.stickY = s_possible_inputs[input_idx].y;
}

static void state_waiting() {
    if (pad::button_pressed(mkb::PAD_TRIGGER_Z)) {
        dump_replay();
    }
    if (mkb::sub_mode_request == mkb::SMD_GAME_PLAY_INIT) {
        sort_inputs_by_dist(s_comparison_pt);
        save_state();
        s_next_input_to_try = 0;
        try_input(s_next_input_to_try);
        s_state = State::TryInput;
    }
}

static void state_try_input() {
    // Check if last input matches

    // Advance to next input if not
    s_next_input_to_try++;
    if (s_next_input_to_try < LEN(s_possible_inputs)) {
        load_state();
        try_input(s_next_input_to_try);
    } else {
        s_state = State::Done;
    }
}

static void state_done() {}

void init() {
    for (u32 i = 0; i < LEN(s_possible_inputs); i++) {
        s8 x = static_cast<s8>(i % 121 - 61);
        s8 y = static_cast<s8>(i / 121 - 61);
        s_possible_inputs[i] = {x, y};
    }
}

void tick() {
    switch (s_state) {
        case State::Waiting:
            state_waiting();
            break;
        case State::TryInput:
            state_try_input();
            break;
        case State::Done:
            state_done();
            break;
    }
}

